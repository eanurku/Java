package com.anurag.charactertype;

public class CharacterTypeDemo {

    public static void main(String[] args) {
        char charValue='U';
        System.out.println(charValue);

        char unicodeCharValue='\u0915';
        char nextUnicodeValue='\u0941';
        System.out.println(unicodeCharValue);
        System.out.println(nextUnicodeValue);
    }
}
