package com.anurag.floatingpointtype;

public class FloatingPointDemo {

    public static void main(String[] args) {
        float floatValue=12.1f;//f is suffix for float type
        double doubleValue=23.12d;//d is suffix for double type

        System.out.println(floatValue);
        System.out.println(doubleValue);
    }
}
